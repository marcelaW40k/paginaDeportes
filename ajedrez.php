   <p><h4>El ajedrez es un deporte que se juega entre dos personas, sobre un tablero, cada una de
    las cuales dispone de 16 piezas móviles que se colocan sobre un tablero dividido en 64 escaques (casillas).
    Al principio del juego cada jugador tiene dieciséis piezas: un rey, una dama, dos alfiles, dos caballos, 
    dos torres y ocho peones. Se trata de un juego de estrategia en el que el objetivo es «derrotar» al rey del oponente.</h4></p>
   <h3>Reglas de juego</h3> 
   <h4>• Las piezas no pueden saltar, en su movimiento, una por encima de la otra (a excepción del caballo, que puede 
   saltar sobre otras, moviendo en ‘L’, y de la torre, en el enroque).</h4>
    <h4>• Una pieza no puede ocupar una casilla ocupada por otra pieza del mismo color, pero sí una ocupada por otra
     del contrario, retirándola del tablero. Esta acción se conoce como “tomar” o “capturar”, y es voluntaria, a 
     decisión del jugador.</h4>
    <h4>• Las casillas a las cuales puede moverse una pieza, se dice que están controladas por ésta. Si una pieza se 
    encuentra dentro de las casillas controladas por una pieza de color contrario, se encuentra atacada por aquélla.</h4>
    <h4>• El rey es la única pieza que no puede moverse o pasar por casillas controladas por piezas contrarias.</h4>
    <h4>Una jugada que ataque al rey se conoce como jaque; es obligatorio para el jugador cuyo rey esté atacado salir 
    del jaque. Si no hay defensa posible contra un jaque, el rey está en jaque mate. Ese es el objetivo y quien lo
    inflige al rey rival, gana.</h4>

